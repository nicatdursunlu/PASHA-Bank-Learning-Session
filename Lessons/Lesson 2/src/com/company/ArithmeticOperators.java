package com.company;

public class ArithmeticOperators {
    public static void main(String[] args) {

        int a = 10, b = 3;

        // addition operator
        System.out.println("a + b = " + (a + b));

        // subtraction operator
        System.out.println("a - b = " + (a - b));

        // multiplication operator
        System.out.println("a * b = " + (a * b));

        // division operator
        System.out.println("a / b = " + (a / b));

        // module operator
        System.out.println("a % b = " + (a % b));
    }
}
